#Modifying element of an array using indexing
fruits = ["Apple","Banana","Mango","Grapes","orange"]
print(fruits)

fruits[1] = "pineapple"
print(fruits)

fruits[-1] = "Guava"
print(fruits)

#output #
#['Apple', 'Banana', 'Mango', 'Grapes', 'orange']
#['Apple', 'pineapple', 'Mango', 'Grapes', 'orange']
#['Apple', 'pineapple', 'Mango', 'Grapes', 'Guava']
